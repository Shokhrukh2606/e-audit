    <p><strong>Ответственность руководства хозяйствующего субъекта</strong></p>
    <p>Руководство хозяйствующего субъекта несет ответственность за составление и достоверность указанной финансовой
        отчетности в соответствии с законодательством о бухгалтерском учете и за систему внутреннего контроля.</p>
    <p><strong>Ответственность аудиторской организации</strong></p>
    <p>Ответственность аудиторской организации заключается в выражении мнения по данной финансовой отчетности на
        основании проведенной аудиторской услуги.</p>
    <p>Мы провели аудиторская услуги прилагаемой финансовой отчетности ООО «DEFACTUM LABORATORIES». Аудиторская услуги
        заключается в оценке достоверности и соответствия во всех существенных аспектах финансовой отчетности и иной
        финансовой информации законодательству о бухгалтерском учете.</p>
    <p>Мы проводили аудиторскую услуги в соответствии с национальными стандартами аудиторской деятельности. Данные
        стандарты требуют планирования и проведения аудиторская услуги таким образом, чтобы получить достаточную
        уверенность в том, что финансовая отчетность не содержит существенных искажений.</p>
    <p>Аудиторская услуги&nbsp; включала проведение аудиторских процедур, направленных на получение аудиторских
        доказательств. Выбор аудиторских процедур является предметом нашего суждения.</p>
    <p>Мы полагаем, что полученные в ходе аудиторской услуги аудиторские доказательства дают достаточные основания для
        выражения мнения о достоверности финансовой отчетности.</p>
    <p><strong>Положительное мнение</strong></p>
    <p>По нашему мнению, финансовая отчетность хозяйствующего субъекта&nbsp; ООО «DEFACTUM
        &nbsp;LABORATORIES»&nbsp;&nbsp;&nbsp;&nbsp; по состоянию на 31-декабрь 2019 года, достоверно отражает во всех
        существенных аспектах финансовое положение, результаты его финансово-хозяйственной деятельности, и соответствует
        требованиям законодательства о бухгалтерском учете. Выявленные оценки экономические показателей соответствует по
        нормы&nbsp; законодательству&nbsp; Республики Узбекистан. Учитывая&nbsp; выше&nbsp; указанные&nbsp; оценки по
        финансовой деятельности&nbsp;&nbsp; хозяйствующего субъекта ООО «DEFACTUM &nbsp;LABORATORIES» &nbsp;считается
        экономически стабильной:</p>
    <p>В связи с этим был сделан положительный вывод по финансовой&nbsp; отчетности хозяйствующего субъекта&nbsp;&nbsp;
        ООО «DEFACTUM LABORATORIES» за период 2019 года.</p>
